package Function_1;

public class LengthOfCircle {
    public static double LengthOfCircle (int radius) {
        double L = 2 * 3.14 * radius;
        System.out.println(L);

        return L;
    }

    public static void main(String[] args) {
        LengthOfCircle(5);
        System.out.println();

    }
}
